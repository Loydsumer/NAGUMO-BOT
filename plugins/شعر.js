const handler = async (m, { command }) => {
  let category;
  switch (command) {
    case 'شعر_حب':
      category = 'حب';
      break;
    case 'شعر_حزن':
      category = 'حزن';
      break;
    case 'شعر_مدح':
      category = 'مدح';
      break;
    case 'شعر_هجاء':
      category = 'هجاء';
      break;
    default:
      return m.reply('✖ استخدم أمر صحيح: شعر_حب، شعر_حزن، شعر_مدح، شعر_هجاء');
  }
  const arr = poems[category];
  const randomPoem = arr[Math.floor(Math.random() * arr.length)];
  await m.reply(`📜 ${randomPoem}`);
};

handler.help = ['شعر_حب','شعر_حزن','شعر_مدح','شعر_هجاء'];
